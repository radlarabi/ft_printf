#include "ft_printf.h"

int	hexa(unsigned int l, char x)
{
	int	a;
	int	b;

	b = 0;
	if (x == 'x')
		a = 87;
	if (x == 'X')
		a = 55;
	if (l >= 16)
	{
		b += hexa(l / 16, x);
		l %= 16;
	}
	if (l < 16)
	{
		if (l >= 10)
			b += ft_putchar(l + a);
		else
			b += ft_putchar(l + '0');
	}
	return (b);
}
