#include "ft_printf.h"

int	ft_putnbr(int n)
{
	long	l;
	int		a;

	a = 0;
	l = (long)n;
	if (l < 0)
	{
		a += ft_putchar('-');
		l *= -1;
	}
	if (l < 10)
	{
		a += ft_putchar(l + '0');
	}
	if (l >= 10)
	{
		a += ft_putnbr(l / 10);
		a += ft_putnbr(l % 10);
	}
	return (a);
}
