#include "ft_printf.h"

int	ft_putnbr_u(unsigned int l)
{
	int	a;

	a = 0;
	if (l >= 10)
	{
		a += ft_putnbr_u(l / 10);
		a += ft_putnbr_u(l % 10);
	}
	if (l < 10)
	{
		a += ft_putchar(l + '0');
	}
	return (a);
}
